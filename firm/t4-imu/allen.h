#ifndef _ALLEN_H_
#define _ALLEN_H_
/**
 * allen_variance.h  -  Allan分散 (ADEV) モジュール
 *
 * τ系列: 10^(0.25*k) [sec], k=0,1,2,...
 *        100kB ring buffer で有効な全τを自動網羅 (計19点)
 *        最小τ=1s, 最大τ≈31622s (≈8.8時間)
 *
 * スレッド安全性: なし。シングルタスク/割り込み禁止での使用を想定。
 */


#include <stdint.h>

/* ------------------------------------------------------------------ */
/*  定数                                                                */
/* ------------------------------------------------------------------ */

/** ring buffer の総バイト数 (= サンプル数, int8_t x 1) */
#define ALLEN_RING_SIZE     25600

/** τ系列の総数 (10^(0.25k), 100kB で有効な全点) */
#define ALLEN_TAU_COUNT     15

/** 計算不能を示すセンチネル値 */
#define ALLEN_RESULT_INVALID  (-1.0f)

/* ------------------------------------------------------------------ */
/*  結果配列                                                            */
/* ------------------------------------------------------------------ */

/**
 * allen_result[i]  :  τ[i] における Allan分散 σ²(τ)
 *
 *   i  τ[sec]
 *   0    1.00
 *   1    1.78
 *   2    3.16
 *   3    5.62
 *   4   10.00
 *   5   17.78
 *   6   31.62
 *   7   56.23
 *   8  100.00
 *   9  177.83
 *  10  316.23
 *  11  562.34
 *  12 1000.00
 *  13 1778.28
 *  14 3162.28
 *  15 5623.41
 *  16 10000.0
 *  17 17782.8
 *  18 31622.8
 *
 * 値が ALLEN_RESULT_INVALID (-1.0f) のとき計算不能。
 */

/* ------------------------------------------------------------------ */
/*  API                                                                 */
/* ------------------------------------------------------------------ */

/**
 * AllenInit()  -  モジュール初期化。起動時に1回だけ呼ぶ。
 *
 * @fc  基準周波数 [Hz]。差分の基点として使用する。
 */
void AllenInit(uint32_t fc);

/**
 * AllenStoreFreq()  -  周波数データを1秒毎に登録する。
 *
 * @freq  現在の周波数計測値 [Hz]
 *
 * 内部処理:
 *   diff = (int32_t)freq - fc
 *   |diff| <= 127  →  int8_t として ring buffer に記録
 *   |diff| >  127  →  INVALID (-128) として記録
 *
 * 計算は行わない。計算は AllenLoop() に委ねる。
 */
void AllenStoreFreq(float freq);

/**
 * AllenLoop()  -  メインループから毎回(できるだけ高頻度に)呼ぶ。
 *
 * 1回の呼び出しで1つの τ を処理して allen_result[] を更新する。
 * 次回呼び出しで次の τ へ進む (ラウンドロビン)。
 * これにより CPU 負荷を時間方向に均等に分散する。
 *
 * スキップ規則:
 *   あるτで invalid サンプルが 10個以上あった場合、
 *   それより大きいτ (= 以降の全インデックス) を ALLEN_RESULT_INVALID
 *   に設定してそのサイクルを打ち切る。
 */
void AllenLoop(void);

int             AllenGetResult(const uint32_t **pTime, float **pResult);
uint32_t        AllenGetMeasureTime(void);
void            AllenCalc(void);

#endif /* ALLEN_VARIANCE_H */
